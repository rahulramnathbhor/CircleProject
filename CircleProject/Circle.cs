using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CircleProject
{
    internal class Circle
    {
        double r, a;
        public Circle()
        {
            r = 1.5;
            

        }
        public Circle(double r)
        { this.r = r;
            

        }
        public double CalculateArea()
        { return a = 3.14 * r * r;

        }
        public double display()
        { return a;
        }   

             
          
    }
}